print('---------1、用索引来修改(一个值)--------------------')


lb列表=['小黑子','开庭','别哭']

lb列表[1]='开艇'        #把索引为1的修改为开艇·

print(lb列表)         #['小黑子', '开艇', '别哭']


print('---------2、用切片来修改(多个值)--------------------')

qp列表=['只因','你','实在','是','太美']
qp列表[0:2]=['鸡','尼','食宰','湿','太美太butterfly']   #把0到1（[0:2]不包括2）相当于删掉再插入等号后面的
print(qp列表)         #['鸡', '尼', '食宰', '湿', '太美太butterfly', '实在', '是', '太美']














